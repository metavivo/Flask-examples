#########################################################################
#                      IMPORT

import my_random

##############################################################################
#                     GLOBAL VARIABLES

global a
global b
global c
global x1
global x2

#########################################################################
#                      PARAMETERS

#########################################################################
#                      CLASSES
#------------------------------------------------------------------------

#########################################################################
#                      FUNCTIONS
#************************************************************************
#                      get_x1

def get_x1():
    return my_random.get_random(11) * my_random.get_random_elem([1, -1])

#************************************************************************
#                      get_x2

def get_x2():
    return my_random.get_random(11) * my_random.get_random_elem([1, -1])

#************************************************************************
#                      get_a

def get_a():
    return my_random.get_random(11) * my_random.get_random_elem([1, -1])

#************************************************************************
#                      get_b

def get_b(a, x1, x2):
    return (-1) * a * (x1 + x2) 

#************************************************************************
#                      get_c

def get_c(a, x1, x2):
    return a * x1 * x2 

#************************************************************************
#                      get_par_string

def get_par_string():
    x1 = get_x1()
    x2 = get_x2()
    a = get_a()
    b = get_b(a, x1, x2)
    c = get_c(a, x1, x2)
    if (a == 0) or (a != 0 and b == 0 and c == 0):
        return get_par_string()
    else:
        return str(a) + ' ' + str(b) + ' ' + str(c) + ' ' + str(x1) + ' ' + str(x2)

